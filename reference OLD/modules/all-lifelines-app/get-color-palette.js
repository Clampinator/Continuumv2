/**
 * Returns the standard hex-color palette for lifeline differentiation.
 * @returns {string[]}
 */
export function getColorPalette() {
    return [
        '#00ccff', // Cyan (Default)
        '#ff00ff', // Magenta
        '#ffd700', // Gold
        '#00ff00', // Green
        '#ff4444', // Red
        '#ff9f43', // Orange
        '#9b59b6', // Purple
        '#ffffff'  // White
    ];
}